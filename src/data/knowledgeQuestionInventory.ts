export function flattenKnowledgeQuestionInventory() {
  return knowledgeQuestionInventory.flatMap((cluster) =>
    cluster.categories.flatMap((category) =>
      category.questions.map((question) => ({
        topicSlug: cluster.topicSlug,
        topicName: cluster.topicName,
        categorySlug: category.categorySlug,
        categoryName: category.categoryName,
        question,
      })),
    ),
  );
}

export function getQuestionInventoryForCluster(
  topicSlug: string,
): KnowledgeQuestionInventoryCluster | undefined {
  return knowledgeQuestionInventory.find((cluster) => cluster.topicSlug === topicSlug);
}

export function getCategoryQuestionCount(
  category: KnowledgeQuestionInventoryCategory,
): number {
  return category.questions.length;
}

export function getCoveragePercent(questionCount: number, targetQuestionCount: number): number {
  if (targetQuestionCount === 0) {
    return 0;
  }

  return Math.round((questionCount / targetQuestionCount) * 100);
}

export function getClusterQuestionCount(topicSlug: string): number {
  const inventory = getQuestionInventoryForCluster(topicSlug);

  if (!inventory) {
    return 0;
  }

  return inventory.categories.reduce(
    (total, category) => total + getCategoryQuestionCount(category),
    0,
  );
}

